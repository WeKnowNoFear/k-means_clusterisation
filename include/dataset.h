#ifndef DATASET_H
#define DATASET_H

#include "point.h"

/*
 * Структура для хранения датасета.
 *
 * points — динамический массив точек
 * size   — количество точек в датасете
 * dim    — размерность пространства (число координат у точки)
 */
typedef struct
{
    Point *points;
    int size;
    int dim;
} Dataset;

/*
 * Загружает датасет из CSV-файла.
 *
 * Поддерживаемый формат:
 *   1.0,2.0
 *   3.5,4.2
 *
 * Особенности:
 * - строки с некорректными данными пропускаются
 * - все точки должны иметь одинаковую размерность
 * - размерность определяется по первой корректной строке
 * - пробелы вокруг чисел игнорируются
 *
 * filename:
 *   путь к CSV-файлу
 *
 * dataset:
 *   структура, в которую будет загружен результат
 *
 * return:
 *   0  — успешная загрузка
 *  -1 — ошибка (файл или память)
 */
int load_dataset(const char *filename, Dataset *dataset);

/*
 * Освобождает память, выделенную под датасет.
 *
 * Освобождаются:
 * - координаты всех точек
 * - массив точек
 *
 * После вызова:
 *   dataset->points = NULL
 *   dataset->size   = 0
 */
void free_dataset(Dataset *dataset);

#endif